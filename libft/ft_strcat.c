#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int	n;
	int	i;

	i = 0;
	n = ft_strlen(s1);
	while (s2[i])
	{
		s1[n + i] = s2[i];
		i++;
	}
	s1[n + i] = '\0';
	return (s1);
}
