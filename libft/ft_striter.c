#include "libft.h"

void	ft_striter(char *s, void (*f) (char *))
{
	int size;
	int i;

	i = 0;
	if (s && f)
	{
		size = ft_strlen(s);
		while (i < size)
		{
			f(&s[i]);
			i++;
		}
	}
}
